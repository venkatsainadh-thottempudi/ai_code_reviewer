def add(a, b):
    """
    add function.

    Args:
        a (type): Description of a.
        b (type): Description of b.

    Returns:
        type: Description of return value.
    """
    return a + b


class Calculator:
    def multiply(self, x, y):
        """
        multiply function.

        Args:
            self (type): Description of self.
            x (type): Description of x.
            y (type): Description of y.

        Returns:
            type: Description of return value.
        """
        return x * y